namespace ProjAddition;
public class Calculator
{  
    public int Add(int n1, int n2)
    {
        return n1+n2;
    }

    public int Sub(int n1, int n2)
    {
        return n1-n2;
    }

    public int Mul(int a, int b)
    {
        return a * b;
    }

    public int Division(int a, int b)
    {
        return a/b;
    }

}
